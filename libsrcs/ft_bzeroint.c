#include "../includes/point.h"

void	ft_bzeroint(void *s, size_t n)
{
	size_t			i;
	int				*s2;

	s2 = (int*)s;
	i = 0;
	while (i < n)
	{
		s2[i] = -1;
		i++;
	}
}
